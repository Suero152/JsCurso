let velocity = 110
const acelleration = 5

do {
  // Bloco de código
  console.log("Estamos na velocidade:" + velocity + "km por segundo.")
  velocity += acelleration
  
} while(velocity <= 100) 
 // A diferença entre Do While e While é que o Do While só avalia se a expressão é true na parte do while. Então pór isso ele executa o bloco do código pelo menos uma vez.
let spaceshipName = 'Colossus'

// Lembre-se que para concatenar strings fazemos isso:
console.log('Nome da nave:', spaceshipName)
console.log('Nome da nave: ' + spaceshipName)

// Com a interpolação de strings isso é facilitado como no exemplo abaixo:

console.log(`Nome da nave: ${spaceshipName}`)

// Podemos também atribuir a uma variável.

let name = `Nome da nave: ${spaceshipName}`
console.log(name)